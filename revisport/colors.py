# colors taken from 
# https://www.geeksforgeeks.org/print-colors-python-terminal/
# https://codehs.com/tutorial/andy/ansi-colors
ECS = "\033"

# errors
RED =  ECS + '[91m'

# successes or action
GREEN = ECS + '[92m'

# warnings
YELLOW = ECS + '[33m'

# user inputs
WHITE = ECS + '[00m'

# tables and report information
PURPLE = ECS + '[95m'
LIGHT_PURPLE = ECS +  '[94m'

# questions
CYAN = ECS + '[96m'